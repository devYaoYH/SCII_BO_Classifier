# 0b[UABETD] 6-bit encoding
# U: is_upgrade?
# A: is_army?
# B: is_building?
# E: for_economy?
# T: for_tech?
# D: for_defense? (static defenses only)
def decode_type(bits,suppress=False):
    flags = ['Static Defense','Technology','Economy','Building','Army','Upgrade']
    if (not suppress):
        for i in range(len(flags)-1,-1,-1):
            if ((1 << i)&bits):
                print('1',end='')
            else:
                print('0',end='')
        print('')
    types = []
    if (bits == 0b000100 or bits == 0):
        if (not suppress):
            print("\tWorker")
        types.append('Worker')
    else:
        for i, flag in enumerate(flags):
            if ((1 << i)&bits):
                if (not suppress):
                    print(f"\t{flag}")
                types.append(flag)
    return ','.join(types)

